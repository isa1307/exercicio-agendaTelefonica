class Usuario{
    constructor(nome, telefone, celular, url, data, email, cep, cidade, instagram, github, id){
        this.nome = nome;
        this.telefone = telefone;
        this.celular= celular;
        this.url = url;
        this.data = data;
        this.email = email;
        this.cep = cep;
        this.cidade = cidade;
        this.instagram = instagram;
        this.github = github;
        this.id = id; 
    }
}